from datetime import datetime
from reportlab.lib.units import mm
from reportlab.platypus import Paragraph, Spacer
from reports.report_styles import (
    ReportDocTemplate, PAGE_W, PAGE_H, MARGIN_LEFT, MARGIN_RIGHT,
    report_title_style, build_info_panel, build_data_table,
    build_summary_box, spacer
)
from database.repositories.repair_repository import RepairRepository


class RepairReport:
    """Generates a repair report for a given date range."""

    def __init__(self, repair_repo: RepairRepository):
        self.repair_repo = repair_repo

    def generate(
        self,
        filepath: str,
        from_date: str,
        to_date: str,
        user_name: str = "System"
    ):
        """Create the PDF report at the given filepath."""
        doc = ReportDocTemplate(filepath, user_name=user_name, pagesize=(PAGE_W, PAGE_H),
                                leftMargin=0, rightMargin=0, topMargin=0, bottomMargin=0)
        story = []

        # Report title
        story.append(Paragraph("REPAIR REPORT", report_title_style))
        story.append(spacer(6 * mm))

        # Information panel
        info = {
            "From Date:": from_date,
            "To Date:": to_date,
            "Generated On:": datetime.now().strftime("%Y-%m-%d %H:%M"),
            "Generated By:": user_name,
        }
        avail_w = PAGE_W - MARGIN_LEFT - MARGIN_RIGHT
        info_table = build_info_panel(info, title="Report Information")
        info_table._argW[0] = 0.4 * avail_w
        info_table._argW[1] = 0.6 * avail_w
        story.append(info_table)
        story.append(spacer(8 * mm))

        # Fetch all active repairs and filter by date
        repairs = self.repair_repo.get_all_active()
        filtered_repairs = [
            r for r in repairs
            if from_date <= r["repair_date"] <= to_date
        ]
        filtered_repairs.sort(key=lambda x: x["repair_date"])

        # Build data table
        if filtered_repairs:
            headers = [
                "Vehicle Number", "Repair Date", "Mileage",
                "Description", "Cost", "Performed By", "Remarks"
            ]
            data_rows = []
            total_cost = 0.0
            for r in filtered_repairs:
                data_rows.append([
                    r.get("vehicle_reg", ""),
                    r["repair_date"],
                    str(r.get("mileage", "")) if r.get("mileage") is not None else "",
                    r["description"],
                    f"{r['cost']:.2f}",
                    r.get("performed_by", ""),
                    r.get("remarks", ""),
                ])
                total_cost += r["cost"]

            col_w = avail_w / len(headers)
            table = build_data_table(headers, data_rows, col_widths=[col_w]*len(headers))
            story.append(table)
            story.append(spacer(10 * mm))

            # Summary box
            summary_items = [
                ("Total Repairs", str(len(filtered_repairs))),
                ("Total Cost", f"PKR {total_cost:,.2f}"),
            ]
            sum_table = build_summary_box(summary_items, title="Repair Summary")
            sum_table._argW[0] = 0.4 * avail_w
            sum_table._argW[1] = 0.6 * avail_w
            story.append(sum_table)
        else:
            story.append(Paragraph(
                "No repairs found for the selected date range.",
                report_title_style
            ))

        doc.build(story)